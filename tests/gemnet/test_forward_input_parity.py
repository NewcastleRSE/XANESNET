# 1. Main-graph edges


def _assert_edge_set_parity(obj, sample, cutoff, edge_index_attr, vec_attr):
    """
    Assert the edges on one graph of a xanesnet sample == brute-force set.
    """
    ei = getattr(sample, edge_index_attr)
    ev = getattr(sample, vec_attr)
    pos = sample.pos
    lat = _lattice_of(obj)
    xn = _edges_from_sample(ei, ev, pos, lat)
    ref = _reference_edges(obj, cutoff)
    assert xn == ref, (
        f"{edge_index_attr} mismatch on {getattr(obj, 'properties', {}).get('file_name', '?')}:"
        f" xn-only={xn - ref}, ref-only={ref - xn}"
    )


def test_main_graph_edges_periodic(periodic_pairs):
    for s, sample in periodic_pairs:
        _assert_edge_set_parity(s, sample, cutoff=3.5, edge_index_attr="edge_index", vec_attr="edge_vec")


def test_main_graph_edges_molecule(molecule_pairs):
    for m, sample in molecule_pairs:
        _assert_edge_set_parity(m, sample, cutoff=3.5, edge_index_attr="edge_index", vec_attr="edge_vec")


