"""
End-to-end parity of ``forward()`` inputs between xanesnet and fairchem.

In xanesnet, all graph indices and basis inputs that the GemNet / GemNet-OC  ``forward`` function consumes 
are produced by ``GemNetDataset.prepare()`` (offline, per sample).  In fairchem they are produced *inside* 
the forward call by ``generate_interaction_graph``, ``get_triplets``, ``get_mixed_triplets`` and ``get_quadruplets``.

This file ports exactly those fairchem in-forward routines and compares their output, end-to-end, 
to what xanesnet serialises into every ``GemNetData`` sample.  
If any of the tensors that the model forward sees does not agree between the two pipelines, these tests fail.

Scope of comparison (as SETS — the two implementations may emit edges in different orders, 
but the set of enumerated edges / triplets / quadruplets must be identical):

1. **Main graph** edges — the ``(src, dst, cell_offset)`` triples enumerated
   by ``build_edges`` must equal a brute-force periodic image enumeration
   at the main cutoff.
2. For **GemNet** (triplets-only) and **GemNet-OC** — id_swap involution,
   compute_triplets output (id3_reduce_ca, id3_expand_ba) against
   ``ref_get_triplets`` on the identical edge set.
3. For **GemNet-OC** only:
   - ``a2ee2a``, ``a2a`` and ``qint`` graphs' edge sets vs brute-force.
   - ``trip_a2e``, ``trip_e2a``, ``trip_e2e`` (mixed triplets) vs
     ``ref_get_mixed_triplets`` on those graphs.
   - Quadruplets ``(id4_reduce_ca, id4_expand_db)`` vs
     ``ref_get_quadruplets``.
   - Per-atom ``target_neighbor_idx`` generated by ``get_inner_idx`` on
     the a2ee2a and a2a target columns.

All of these are value-level checks — any mismatch makes the GemNet / GemNet-OC forward 
pass see different inputs than the fairchem reference would see.
"""

