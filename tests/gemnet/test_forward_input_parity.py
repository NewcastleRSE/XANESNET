# 3. id_swap involution + vector negation (identical to fairchem's ``reorder_symmetric_edges`` post-condition)


def test_id_swap_is_fairchem_compatible_periodic(periodic_pairs):
    for _s, sample in periodic_pairs:
        swap = sample.id_swap
        nE = sample.edge_index.size(1)
        assert swap.size(0) == nE
        assert swap[swap].tolist() == list(range(nE))
        assert torch.allclose(sample.edge_vec[swap], -sample.edge_vec, atol=1e-5)
        # reverse of (s,d) must be (d,s)
        assert torch.equal(sample.edge_index[0, swap], sample.edge_index[1])
        assert torch.equal(sample.edge_index[1, swap], sample.edge_index[0])


def test_id_swap_is_fairchem_compatible_molecule(molecule_pairs):
    for _m, sample in molecule_pairs:
        swap = sample.id_swap
        nE = sample.edge_index.size(1)
        assert swap[swap].tolist() == list(range(nE))
        assert torch.equal(sample.edge_index[0, swap], sample.edge_index[1])


# 4. Main-graph triplets (id3_reduce_ca / id3_expand_ba) vs fairchem


